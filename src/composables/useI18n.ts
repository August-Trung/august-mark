export function useI18n() {
  const settingsStore = useSettingsStore()

  const currentLang = computed(() => {
    return settingsStore.getSettingValue<'en' | 'vi'>('language', 'en')
  })

  function t(
    path: string,
    fallbackOrVariables?: string | Record<string, string | number>,
    variables?: Record<string, string | number>
  ): string {
    const fallbackText = typeof fallbackOrVariables === 'string' ? fallbackOrVariables : ''
    const vars = typeof fallbackOrVariables === 'object' ? fallbackOrVariables : variables

    const keys = path.split('.')
    let translation: any = translations[currentLang.value] || translations.en

    for (const key of keys) {
      if (translation && translation[key] !== undefined) {
        translation = translation[key]
      } else {
        // Fallback to English first
        let fallback: any = translations.en
        let found = true
        for (const fbKey of keys) {
          if (fallback && fallback[fbKey] !== undefined) {
            fallback = fallback[fbKey]
          } else {
            found = false
            break
          }
        }
        if (found) {
          return replaceVariables(fallback, vars)
        }
        return replaceVariables(fallbackText || path, vars)
      }
    }

    return replaceVariables(translation, vars)
  }

  function replaceVariables(text: string, variables?: Record<string, string | number>): string {
    if (!variables || typeof text !== 'string') return text
    let result = text
    for (const key in variables) {
      result = result.replace(`{${key}}`, String(variables[key]))
    }
    return result
  }

  return {
    t,
    currentLang
  }
}
